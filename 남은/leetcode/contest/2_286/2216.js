var minDeletion = function (nums) {
  let answer = 0;
  if (!nums.length) return 0;

  for (let idx = 0; idx < nums.length; idx++) {
    // console.log({ nums });
    if (idx % 2 === 0 && nums[idx] === nums[idx + 1]) {
      //   nums.splice(idx, 1);
      nums.shift();
      idx--;
      answer++;
    }
  }

  if (nums.length % 2 !== 0) answer++;

  return answer;
};

